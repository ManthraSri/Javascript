let a = 10;         // Number
let b = "Hello";    // String
let c = true;       // Boolean
let d = null;       // Null
let e;              // Undefined
let f = {x: 1, y: 2}; // Object

console.log(typeof a, typeof b, typeof c, typeof d, typeof e, typeof f);


// For loop
for (let i = 1; i <= 5; i++) {
    console.log("For Loop:", i);
}

// While loop
let j = 1;
while (j <= 3) {
    console.log("While Loop:", j);
    j++;
}

// Do-while loop
let k = 1;
do {
    console.log("Do-While Loop:", k);
    k++;
} while (k <= 2);
