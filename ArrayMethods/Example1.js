let fruits = ["Apple", "Banana", "Cherry"];

fruits.push("Mango");  // Adds at the end
console.log(fruits);
fruits.unshift("Orange");  // Adds at the beginning
console.log(fruits);

console.log(fruits.pop());  // Removes the last element 

console.log(fruits.shift());  // Removes the first element 

console.log(fruits.indexOf("Banana"));  //find index

console.log(fruits.includes("Mango"));  // Checks if  exists
